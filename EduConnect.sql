DROP DATABASE EduConnect
CREATE DATABASE EduConnect
USE EduConnect

SET DATEFORMAT DMY

CREATE TABLE LOGIN 
(
	ID INT IDENTITY NOT NULL PRIMARY KEY,
	USERNAME VARCHAR(255),
	USERPASS VARCHAR(255),
)

CREATE TABLE ADMIN (
	MAAD INT IDENTITY NOT NULL PRIMARY KEY,
	TENAD NVARCHAR(255),
	MALOGIN INT FOREIGN KEY REFERENCES LOGIN(ID),
)


CREATE TABLE HOCSINH (
	MAHS INT IDENTITY NOT NULL PRIMARY KEY,
	HOTEN NVARCHAR(255),
	NTNS DATE,
	NOISINH NVARCHAR(255),
	GIOITINH BIT,
	DANTOC NVARCHAR(255),
	TONGIAO NVARCHAR(255),
	CHINHSACH NVARCHAR(255),
	SDT VARCHAR(30),
	GHICHU NVARCHAR(255),
	SOHIEU VARCHAR(255),
	DIACHI NVARCHAR(255),
	NIENKHOA VARCHAR(20),
	MALOGIN INT FOREIGN KEY REFERENCES LOGIN(ID),
)

CREATE TABLE PHUHUYNH (
	MAHS INT FOREIGN KEY REFERENCES HOCSINH (MAHS),
	HOTENBO NVARCHAR(255),
	HOTENME NVARCHAR(255),
	NGHEBO NVARCHAR(255),
	NGHEME NVARCHAR(255),
	SDTBO VARCHAR(30),
	SDTME VARCHAR(30),
	CONSTRAINT PK_PHUHUYNH PRIMARY KEY (MAHS),
)

CREATE TABLE TO1(
	MATO INT IDENTITY NOT NULL PRIMARY KEY,
	TENTO NVARCHAR(255),
	SOLUONG INT,
	TOTRUONG INT, 
)

CREATE TABLE GIAOVIEN (
	MAGV INT IDENTITY NOT NULL PRIMARY KEY,
	HOTEN NVARCHAR(255),
	EMAIL VARCHAR(255),
	SDT VARCHAR(30),
	DIACHI NVARCHAR(255),
	NTNS DATE,
	GIOITINH BIT,
	MALOGIN INT FOREIGN KEY REFERENCES LOGIN(ID),
	MATO INT FOREIGN KEY REFERENCES TO1(MATO),
)

ALTER TABLE TO1 ADD CONSTRAINT FK_TO FOREIGN KEY (TOTRUONG) REFERENCES GIAOVIEN(MAGV)

CREATE TABLE KHOI (
	MAKHOI INT IDENTITY NOT NULL PRIMARY KEY,
	TENKHOI NVARCHAR(255),
)

CREATE TABLE LOP (
	MALOP INT IDENTITY NOT NULL PRIMARY KEY,
	TENLOP VARCHAR(10),
	SISO INT,
	NAMHOC VARCHAR(10),
	GVCN INT FOREIGN KEY REFERENCES GIAOVIEN(MAGV),
	LOPTRUONG INT FOREIGN KEY REFERENCES HOCSINH(MAHS),
	MAKHOI INT FOREIGN KEY REFERENCES KHOI(MAKHOI),
)

CREATE TABLE HOCTAP (
	MAHT INT IDENTITY NOT NULL PRIMARY KEY,
	MAHS INT FOREIGN KEY REFERENCES HOCSINH(MAHS),
	MALOP INT FOREIGN KEY REFERENCES LOP(MALOP),
)

CREATE TABLE LOAIDIEM (
	MALD INT IDENTITY NOT NULL PRIMARY KEY,
	TENLD NVARCHAR(20),
	HESO INT,
)

CREATE TABLE MONHOC (
	MAMH INT IDENTITY NOT NULL PRIMARY KEY,
	TENMH NVARCHAR(255),
)

CREATE TABLE THI (
	MATHI INT IDENTITY NOT NULL PRIMARY KEY,
	MAHS INT FOREIGN KEY REFERENCES HOCSINH(MAHS),
	MALOP INT FOREIGN KEY REFERENCES LOP(MALOP),
	MAMH INT FOREIGN KEY REFERENCES MONHOC(MAMH),
	MALD INT FOREIGN KEY REFERENCES LOAIDIEM(MALD),
	DIEM VARCHAR(4),
	HOCKY INT,
	--CONSTRAINT PK_THI PRIMARY KEY (MAHS, MALOP, MAMH, MALD, HOCKY),
)

CREATE TABLE GIANGDAY (
	MAGV INT FOREIGN KEY REFERENCES GIAOVIEN(MAGV),
	MAMH INT FOREIGN KEY REFERENCES MONHOC(MAMH),
	MALOP INT FOREIGN KEY REFERENCES LOP(MALOP),
	HOCKY INT,
	CONSTRAINT PK_GIANGDAY PRIMARY KEY (MAGV, MAMH, MALOP, HOCKY),
)

CREATE TABLE THANHTICH (
	MATT INT IDENTITY NOT NULL PRIMARY KEY,
	TENTT NVARCHAR(255),
	MAHS INT FOREIGN KEY REFERENCES HOCSINH(MAHS),
	MALOP INT FOREIGN KEY REFERENCES LOP(MALOP),
)

CREATE TABLE KETQUA (
	MAHS INT FOREIGN KEY REFERENCES HOCSINH(MAHS),
	MALOP INT FOREIGN KEY REFERENCES LOP(MALOP),
	HOCKY INT, 
	DTB NUMERIC(3,1),
	HANHKIEM NVARCHAR(30),
	XEPLOAI NVARCHAR(30),
	CONSTRAINT PK_KETQUA PRIMARY KEY (MAHS, MALOP, HOCKY),
)

CREATE TABLE TBMON (
	MAHS INT FOREIGN KEY REFERENCES HOCSINH(MAHS),
	MALOP INT FOREIGN KEY REFERENCES LOP(MALOP),
	MAMH INT FOREIGN KEY REFERENCES MONHOC(MAMH),
	HOCKY INT,
	DTB VARCHAR(4),
	CONSTRAINT PK_TBMON PRIMARY KEY (MAHS, MAMH, MALOP, HOCKY),
)

CREATE TABLE NHANXET (
	MAHS INT FOREIGN KEY REFERENCES HOCSINH(MAHS),
	MALOP INT FOREIGN KEY REFERENCES LOP(MALOP),
	HOCKY INT, 
	NHANXET NVARCHAR(255),
	CONSTRAINT PK_NHANXET PRIMARY KEY (MAHS, MALOP, HOCKY),
)

